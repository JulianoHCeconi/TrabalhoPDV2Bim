    private void tfIdClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIdClienteKeyPressed
        
        if(evt.getKeyCode() == evt.VK_ENTER){
            if(!tfIdCliente.getText().equals("")){
                
                try{
                    
                    Long id = Long.parseLong(tfIdCliente.getText());
                    
                    ClienteDTO cliente = ClienteService.buscaId(id);
                    
                    tfNome.setText(cliente.getNome());
                    tfTelefone.setText(cliente.getTelefone());
                    tfEmail.setText(cliente.getEmail());
                    
                }catch(Exception ex){
                    Logger.getLogger(TelaDeVendaFrame.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                }
                
            }
            
            
        }
        
    }//GEN-LAST:event_tfIdClienteKeyPressed

    private void tfProdutoIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfProdutoIdKeyPressed
        
        if(evt.getKeyCode() == evt.VK_ENTER){
            if(!tfProdutoId.getText().equals("")){
                
                try{
                    
                    Long id = Long.parseLong(tfProdutoId.getText());
                    
                    ProdutoDTO produto = ProdutoService.buscaId(id);
                    
                    tfDescricao.setText(produto.getDescricao());
                    tfCategoriaProduto.setText(produto.getCategoria());
                    tfValorProduto.setText(String.valueOf(produto.getValorUnitario()));

                    
                }catch(Exception ex){
                    
                    Logger.getLogger(TelaDeVendaFrame.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Produto não encontrado!");
                }
                
            }
            
            
        }
        
    }//GEN-LAST:event_tfProdutoIdKeyPressed

